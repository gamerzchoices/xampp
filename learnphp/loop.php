<?php

// For Loop

for ($x = 1; $x <= 10; $x++) {
    echo "Prabesh Gupta<br>";
}


//While Loop

$y = 10;
while ($y > 0) {
    echo $y . "<br>";
    $y--;

}

//Do while loop (Same as while loop but it prints the statement atleast once before checking the condition)

$z = 22;

do {
    echo "<b>" . $z . "</b><br>";
    $z--;
} while ($z > 22);


?>